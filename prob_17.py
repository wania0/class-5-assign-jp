# Find the Length of Each String in a List
# Objective: Write a Python program that finds and prints the length of each string in a given list.
# Example list
# strings = ["apple", "banana", "cherry", "date"]

# SOLUTION:

strings = ["apple", "banana", "cherry", "date"]

for i in strings:
    print("Length of", i ,"is:", len(i))